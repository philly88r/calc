/**
 * Helper function to find a matching gate based on specifications
 * @param {Object} specs - The gate specifications to match
 * @param {string} specs.type - The type of gate (single, double, sliding)
 * @param {string} specs.commercial - Whether the gate is commercial or residential
 * @param {boolean} specs.barbWire - Whether the gate has barb wire
 * @param {string} specs.material - The material of the gate (e.g., "galvanized 1 5/8\"")
 * @param {string} specs.height - The height of the gate (e.g., "6'")
 * @param {string} specs.width - The width of the gate (e.g., "4")
 * @returns {Object|null} The matching gate data or null if no match is found
 */
export const findMatchingGate = (specs) => {
  // Determine the type of gate based on input parameters
  let gateType = '';
  
  if (specs.commercial === 'commercial') {
    gateType += 'Commercial ';
  } else {
    gateType += 'Residential ';
  }
  
  if (specs.type === 'single') {
    gateType += 'single gate ';
  } else if (specs.type === 'double') {
    gateType += 'double gate ';
  } else if (specs.type === 'sliding') {
    gateType += 'sliding gate ';
  }
  
  if (specs.barbWire) {
    gateType += 'with barb wire';
    
    // For gates with barb wire, the height format is different
    if (!specs.height.includes('plus')) {
      // Convert height format from "6'" to "6' plus 1" for barbed wire gates
      specs.height = specs.height.replace("'", "' plus 1");
    }
  } else {
    gateType += 'no barb wire';
  }
  
  // Extract the basic material info without the quote character for more flexible matching
  let materialBase = '';
  if (specs.material) {
    // Remove any trailing quote characters for consistency
    materialBase = specs.material.replace(/\"+$/, '');
    gateType += ` - ${materialBase}`;
  }
  
  console.log("Gate type string constructed:", gateType);
  console.log("Looking for height:", specs.height);
  console.log("Looking for width:", specs.width);
  
  // First try to find an exact match
  const exactMatches = gateData.filter(gate => {
    // For more flexible matching, we'll normalize the gate name by removing extra spaces
    const normalizedGateName = gate.name.replace(/\s+/g, ' ').trim();
    const normalizedSearchType = gateType.replace(/\s+/g, ' ').trim();
    
    const nameMatch = normalizedGateName.includes(normalizedSearchType);
    const heightMatch = gate.height === specs.height;
    const widthMatch = gate.width === specs.width.toString();
    
    // Log detailed matching info for debugging
    if (nameMatch) {
      console.log("Name match found:", gate.name);
      console.log("Height match?", heightMatch, "Gate height:", gate.height, "Spec height:", specs.height);
      console.log("Width match?", widthMatch, "Gate width:", gate.width, "Spec width:", specs.width.toString());
    }
    
    return nameMatch && heightMatch && widthMatch;
  });
  
  console.log("Exact matches found:", exactMatches.length);
  
  // If we have an exact match, return it
  if (exactMatches.length > 0) {
    console.log("Returning exact match:", exactMatches[0]);
    return exactMatches[0];
  }
  
  // If no exact match, find gates that match the name pattern
  const typeMatches = gateData.filter(gate => gate.name.includes(gateType));
  
  if (typeMatches.length === 0) {
    return null; // No gates of this type found
  }
  
  // Find gates with matching height but different width
  const heightMatches = typeMatches.filter(gate => gate.height === specs.height);
  
  if (heightMatches.length > 0) {
    // Sort by width to find the next closest width
    const sortedByWidth = [...heightMatches].sort((a, b) => {
      const aWidth = parseInt(a.width);
      const bWidth = parseInt(b.width);
      const targetWidth = parseInt(specs.width);
      
      // Sort by absolute difference from target width
      return Math.abs(aWidth - targetWidth) - Math.abs(bWidth - targetWidth);
    });
    
    return sortedByWidth[0]; // Return the closest width match
  }
  
  // If no height match, find the next closest height with matching type
  const sortedByHeight = [...typeMatches].sort((a, b) => {
    // Extract numeric height (e.g., "6'" -> 6, "7' plus 1" -> 7)
    const getHeightValue = (heightStr) => {
      const match = heightStr.match(/(\d+)'/);
      return match ? parseInt(match[1]) : 0;
    };
    
    const aHeight = getHeightValue(a.height);
    const bHeight = getHeightValue(b.height);
    const targetHeight = getHeightValue(specs.height);
    
    // Sort by absolute difference from target height
    return Math.abs(aHeight - targetHeight) - Math.abs(bHeight - targetHeight);
  });
  
  if (sortedByHeight.length > 0) {
    // Find gates with the closest height
    const closestHeight = sortedByHeight[0].height;
    const closestHeightGates = typeMatches.filter(gate => gate.height === closestHeight);
    
    // Sort by width to find the closest width match
    const sortedByWidth = [...closestHeightGates].sort((a, b) => {
      const aWidth = parseInt(a.width);
      const bWidth = parseInt(b.width);
      const targetWidth = parseInt(specs.width);
      
      // Sort by absolute difference from target width
      return Math.abs(aWidth - targetWidth) - Math.abs(bWidth - targetWidth);
    });
    
    return sortedByWidth[0]; // Return the closest width match with closest height
  }
  
  // If we still don't have a match, return null
  return null;
};

